package lesson_12;

public class Example01 {

    public static void main(String[] args) {
        int a = 100;
        int b = 120;

        System.out.println(a == b); //false
        System.out.println(a != b); //true
        System.out.println(a > b); //false
        System.out.println(a < b); //true
        System.out.println(a >= b); //false
        System.out.println(a <= b); //true
    }
}
